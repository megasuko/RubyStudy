    elsif n<100000
        r=i*10000+h*1000+g*100+f*10+e
    elsif n<1000000
        r=i*100000+h*10000+g*1000+f*100+e*10+d
    elsif n<10000000
        r=i*1000000+h*100000+g*10000+f*1000+e*100+d*10+c
