    g=n/100-(a*1000000+b*100000+c*10000+d*1000+e*100+f*10)
    h=n/10-(a*10000000+b*1000000+c*100000+d*10000+e*1000+f*100+g*10)
    i=n-(a*100000000+b*10000000+c*1000000+d*100000+e*10000+f*1000+g*100+h*10)
